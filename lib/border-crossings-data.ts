/** Get crossings filtered by region */
export function getCrossingsByRegion(region: BorderCrossing["region"]): BorderCrossing[] {
  return BORDER_CROSSINGS.filter((c) => c.region === region);
}

/** Get the top N most popular crossings */
export function getTopCrossings(n: number = 10): BorderCrossing[] {
  return [...BORDER_CROSSINGS].sort((a, b) => a.popularityRank - b.popularityRank).slice(0, n);
}

/** Get RV-friendly crossings (rating 3) */
export function getRVFriendlyCrossings(): BorderCrossing[] {
  return BORDER_CROSSINGS.filter((c) => c.rvFriendly === 3);
}

