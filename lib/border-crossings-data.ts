export interface BorderCrossing {
  id: string;
  name: string;
  canadianCity: string;
  canadianProvince: string;
  usCity: string;
  usState: string;
  /** true = open 24/7 */
  open24h: boolean;
  /** RV-friendly rating: 1-3 (1=tight/not ideal, 2=ok, 3=great for RVs) */
  rvFriendly: number;
  /** Typical wait time description */
  typicalWait: string;
  /** Notes specific to RVers */
  rvNotes: string;
  /** CBSA office phone (Canadian side) */
  cbsaPhone: string;
  /** CBP port phone (US side) — empty if not a staffed CBP port */
  cbpPhone: string;
  /** Latitude for map reference */
  lat: number;
  /** Longitude for map reference */
  lng: number;
  /** Region grouping */
  region: "Atlantic" | "Quebec" | "Ontario" | "Prairies" | "British Columbia" | "Alaska";
  /** Popularity rank (1 = busiest) */
  popularityRank: number;
  /** Whether this crossing has a dedicated RV/truck lane */
  hasRVLane: boolean;
}

export interface BorderLaw {
  id: string;
  rank: number;
  title: string;
  icon: string;
  severity: "critical" | "high" | "medium";
  /** Fine range or penalty description */
  penalty: string;
  description: string;
  /** Detailed tips for RVers */
  tips: string[];
  /** Which direction this applies: "entering_canada", "entering_us", "both" */
  direction: "entering_canada" | "entering_us" | "both";
}

